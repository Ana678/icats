                                    <h6><b>Qual a finalidade desse projeto?</b></h6> <br>
                                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Atendendo à necessidade fundamental de se preservar a qualidade de vida dos gatos de estimação pelo mundo, nossa equipe tomou essa iniciativa de promover, por meio de um sistema web, a facilitação desse processo. 
                                    Assim, estamos há algumas semanas empenhados no processo de produção do site "icats", um completo e intuitivo portal para que os usuários donos de felinos se cadastrem e realizem o crucial acompanhamento do estado de saúde de seus animais de estimação. 
                                    Ademais, isso é possível por meio do cadastro, feito pelo usuário conforme ele desejar, do peso e humor de seu gato em um determinado dia, sendo esse humor uma medição de características perceptíveis do gato (se ele está triste ou animado, por exemplo). Logo, esperamos permear um sistema proveitoso e que transforme a vida dos usuários, bem como de seus gatos, para melhor.
                                    </p>
                                    <br><br>
                                    <h6><b>Missão</b></h6> <br>
                                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Promover um canal de acompanhamento do estado de saúde dos preciosos gatos dos usuários, que precisam de cuidados básicos com sua saúde, para que possam continuar sendo a companhia alegre de seus donos.
                                    </p>
                                    <br><br>
                                    <h6><b>Visão</b></h6> <br>
                                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Fornecer um ambiente no qual os gatos recebam cuidados de saúde concretos, cedidos por seus donos.
                                    </p>
                                    <br><br>
                                    <h6><b>Valores</b></h6> <br>
                                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;• Facilitar o acompanhamento do estado de saúde dos felinos. 
                                    <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;• Proporcionar as facilidades e benefícios da internet em um sistema prático. 
                                    <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;• Apoiar a criação de gatos e promoção de seus bem-estares. 
                                    </p>
                                    <br><br>
                                    <h6><b>Conheça Nossa equipe</b></h6> <br>
                                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A equipe do sistema "icats" é composta por quatro alunas do curso de informática, do IFRN -  Campus Caicó, uma instituição que há anos promove o conhecimento técnico de qualidade. Assim, caminhando nessa longa jornada dos quatro anos de conclusão do curso, essa equipe de programadoras procura, unidas, resolver as necessidades de construção desse projeto, que foi baseado no uso do "PAS - Processo Acadêmico Simplificado". Com isso, esperamos realmente que o usuário possa fazer um bom uso do sistema, e aproveite suas funcionalidades.
